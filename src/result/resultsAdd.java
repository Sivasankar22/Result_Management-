    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String physics = jTextField2.getText();
        String m3 = jTextField3.getText();
        String os = jTextField4.getText();
        String dbms = jTextField5.getText();
        String em = jTextField6.getText();
        
        try{
          Class.forName("com.mysql.jdbc.Driver");
          Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sres","root","root");
          Statement st = conn.createStatement();
          ResultSet rs = st.executeQuery("select * from student where RollNo='"+roll+"'");
          
          if(rs.next()){
          st.executeUpdate("insert into result(RollNo,Physics,M3,OS,DBMS,Em) values("+roll+",'"+physics+"','"+m3+"','"+os+"','"+dbms+"','"+em+"')");
          JOptionPane.showMessageDialog(null, "marks updated successfully");
          setVisible(false);
          new resultsAdd().setVisible(true);
          }
          else{
            JOptionPane.showMessageDialog(null, "entered roll no is Invalid");  
          }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

